// String
var i = "Hello";
console.log("data type : ", typeof (i));
console.log(i);

// Number
var a = 7;
console.log("data type : ", typeof (a));
console.log(a);

// Boolean
var b = true;
console.log(b);
console.log("data type : ", typeof (b));

// undefined
var c;
console.log(c);
console.log("data type : ", typeof (c));

// null
var d = null;
console.log(d);
console.log("data type : ", typeof (d));

