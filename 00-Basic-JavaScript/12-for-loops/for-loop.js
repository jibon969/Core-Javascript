
// Example
for (i = 0; i < 5; i++) {
    console.log(i)
    console.log('\n');
}

// Print even
for (i = 0; i <= 10; i++) {
    if (i % 2 == 0) {
        console.log("Even number", i)
        console.log('\n');
    }
}


// Print odd number
for (i = 0; i <= 10; i++) {
    if (i % 2 == 1) {
        console.log("odd number", i)
    }
}
