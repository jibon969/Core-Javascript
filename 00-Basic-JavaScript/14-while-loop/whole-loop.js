
// while loop

var i = 0;
while (i <= 5) {
    console.log(i);
    i++;
}
console.log('\n');

// even
var i = 0;
while (i <= 5) {
    if (i % 2 == 0) {
        console.log(i);
    }
    i++;
}

//  odd
var i = 0;
while (i <= 5) {
    if (i % 2 == 1) {
        console.log(i);
    }
    i++;
}

// multipation table
